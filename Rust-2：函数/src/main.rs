fn f(x: i32) -> i32 {x + 1}
//      ^^^-1   ^^^-2
// 1:是传入的x参数的类型
// 2:是返回值的类型

fn main() {
    println!("{}", f({let y = 1; y + 1}));
    //       ^^^     ^^^^^^^^^^^^^^^^^^
    //       |       |
    //       |       └── 块表达式：创建变量y=1，然后返回y+1的值(即2)作为f函数的参数
    //       └── println!宏的格式化占位符，用于显示f函数的返回值
}

